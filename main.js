function validateForm() {
    
    let name = document.getElementsByClassName('nameField')[0].value;
    let email = document.getElementsByClassName('emailField')[0].value;
    let msg = document.getElementsByClassName('msgField')[0].value;
    /* let validEmailRegex = /^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.-[a-zA-Z0-9-]+)*$/; */
    let validEmailRegex = /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
    if(name && email.match(validEmailRegex) && msg){
        console.log("form is ok")
        document.getElementsByClassName('success_msg')[0].classList.add("success_msg2")
        document.getElementsByClassName('nameField')[0].value='';
        document.getElementsByClassName('emailField')[0].value='';
        document.getElementsByClassName('msgField')[0].value='';
        document.getElementsByClassName('nameError')[0].style.display='none';
        document.getElementsByClassName('emailError')[0].style.display='none';
        document.getElementsByClassName('msgError')[0].style.display='none';
    }else{
        console.log(name)
        console.log("form is not ok")
        document.getElementsByClassName('success_msg')[0].classList.remove("success_msg2")
        if(!name && !email.match(validEmailRegex) && !msg){
            document.getElementsByClassName('nameError')[0].style.display='block';
            document.getElementsByClassName('emailError')[0].style.display='block';
            document.getElementsByClassName('msgError')[0].style.display='block';
        }else if(!name){
            document.getElementsByClassName('nameError')[0].style.display='block';
        }else if(!email.match(validEmailRegex)){
            document.getElementsByClassName('emailError')[0].style.display='block';
        }else if(!msg){
            document.getElementsByClassName('msgError')[0].style.display='block';
        }else if(name){
            console.log("name")
            document.getElementsByClassName('nameError')[0].style.display='none';
        }else if(email.match(validEmailRegex)){
            console.log("name")
            document.getElementsByClassName('emailError')[0].style.display='none';
        }else if(msg){
            console.log("name")
            document.getElementsByClassName('msgError')[0].style.display='none';
        }
        console.log("end")
     }
    return false;
  }

